package week1.day2;

public class Calculator {
	
	public int sumOfNumbers(int n1, int n2, int n3)
	{
		return n1+n2+n3;
	}
	
	public int subOfNumbers(int n1, int n2)
	{
		return n1-n2;
	}
	
	public double mulOfNumbers(double n1, double n2)
	{
		return n1*n2;
	}
	
	public float divisionOfNumbers(float n1, float n2)
	{
		return n1/n2;
	}


}
